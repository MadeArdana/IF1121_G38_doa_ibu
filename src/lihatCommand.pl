lihatCommand :-
    write('Berikut adalah perintah umum yang dapat digunakan dalam permainan: '), nl,
    write('- startGame.             : Memulai permainan baru.'), nl,
    write('- lihatCommand.          : Menampilkan daftar perintah ini.'), nl,
    write('- mainkanKartu(Nomor).   : Memainkan kartu berdasarkan nomor urut di tangan.'), nl,
    write('- ambilKartu.            : Mengambil satu kartu dari tumpukan (deck).'), nl,
    write('- lihatKartu.            : Melihat daftar kartu yang ada di tangan Anda saat ini.'), nl,
    write('- cekInfo.               : Melihat informasi permainan (giliran, arah, warna aktif, kartu teratas).'), nl,
    write('- uni(Nomor).            : Memanggil "UNI" bersamaan dengan memainkan kartu kedua terakhir (tersisa 1 kartu).'), nl,
    write('- tangkap(NamaPemain).   : Menangkap pemain yang lupa memanggil "UNI".'), nl,
    write('- saveGame.              : Menyimpan progres permainan saat ini.'), nl,
    write('- loadGame.              : Memuat progres permainan yang sebelumnya disimpan.'), nl.
